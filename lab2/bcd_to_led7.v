module bcd_to_led7 (
    input  [3:0] in,   // index 0..4
    output [6:0] out
);

assign out = (in == 3'd0) ? 7'b1000000 :  // 0
             (in == 3'd1) ? 7'b1111001 :  // 1
             (in == 3'd2) ? 7'b0100100 :  // 2
             (in == 3'd3) ? 7'b0110000 :  // 3
             (in == 3'd4) ? 7'b0011001 :  // 4
				 (in == 3'd5) ? 7'b0010010 :  // 5
             (in == 3'd6) ? 7'b0000010 :  // 6
             (in == 3'd7) ? 7'b1111000 :  // 7
             (in == 3'd8) ? 7'b0000000 :  // 8
                            7'b0010000;   // 9

endmodule